// A method of solving a complex problem by breaking it down into a collection of
// simpler subproblems, solving each of those subproblems just once, and storing
// their solution.

// Another way to put it: "Using past knowledge to reduce computation of the same
// problem."

// It works when you have 'overlapping subproblems' and 'optimal substructure.'

// Overlapping subproblems
// When you can break a problem down into parts that have
// similar if not identical issues. Fibonacci sequence is a good example of this.
// Mergesort is almost NEVER a good example of this.

// Optimal substructure.
// A problem is said to have optimal substructure if an optimal solution can be constructed
// from the optimal solutions of its subproblems. Fib but also shortest path.
