// Think -> Build -> Architect.

// Think about the layout of your webpage or web app before writing code.
// Build your layout in HTML and CSS with a consistent stucture for naming classes.
// Create a logical architecture for your CSS with files and folders.

// Component-driven design.
//   Modular building blocks that make up interfaces.
//   Held together by the layout of the page.
//   Re-usable across a project, and between different projects.
//   Independent, allowing us to use them anywhere on the page.

// BEM - Block Element Modifier
// A block is a standalone component that is meaningful on its own.
// An element is a part of a block that has no meaning on its own.
// Modifier is a different version of a block or an element.

// The 7-1 Pattern
// 7 different folders for partial sass files and 1 main sass file to import all
// other files into a compiled CSS stylesheet.

// The 7 Folders
// base/
// components/
// layout/
// pages/
// themes/
// abstracts/
// vendors/
