// Normal Flow - Default positioning scheme. Not floated. Not absolutely positioned.
// Elements laid out according to their source order.

// Floats - Element is removed from the normal flow. Text and inline elements will
// wrap around the floated element. The container will not adjust its height to
// the element.

// Absolute - Element is removed from the normal flow. No impact on surrounding
// contents or elements. We use top, bottom, left, and right to offset the element
// from its relatively positioned container.

// Stacking contexts - determine in which order overlapping contents are displayed
// on the page. Z-index would be an example of a way to amend this.
